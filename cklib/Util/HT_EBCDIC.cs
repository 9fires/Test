        //-----------------------------------------------------------------------------
        #region EBCDIC→string変換
        /// <summary>
		/// EBCDIC→String変換
		/// </summary>
		/// <param name="cb">EBCDICデータ</param>
		/// <returns>string</returns>
		public	static	string	ToString(byte[]	cb)
		{
            return  ToString(cb, 0, cb.Length);
		}

		/// <summary>
		/// EBCDIC→String変換
		/// </summary>
		/// <param name="cb">EBCDICデータ</param>
		/// <param name="offset">変換開始位置</param>
		/// <returns>string</returns>
        public static string ToString(byte[] cb, int offset)
        {
            return ToString(cb, offset, cb.Length);
        }
		/// <summary>
		/// EBCDIC→String変換
		/// </summary>
		/// <param name="cb">EBCDICデータ</param>
		/// <param name="offset">変換開始位置</param>
		/// <param name="length">変換長</param>
		/// <returns>string</returns>
		public	static	string	ToString(byte[]	cb,int	offset,int length)
		{
			byte[]	tmp	=	new	byte[length];
			int	i,j;
			for	(j=0,i=offset;(i<cb.Length)&&(i<(offset+length));i++,j++)
			{
				tmp[j]	=	EbcdicToJis8[cb[i]];
			}
			return	System.Text.Encoding.GetEncoding("Shift_JIS").GetString(tmp,0,j);
		}
        #endregion EBCDIC→string変換
        //-----------------------------------------------------------------------------
        #region	string→EBCDIC変換
        /// <summary>
        /// string→EBCDIC変換
        /// </summary>
        /// <param name="str">変換元文字列</param>
        /// <returns>変換結果バイト配列</returns>
        public	static	byte[]	ToEbcdic(string		str)
		{
			byte[]	cb	 =	System.Text.Encoding.GetEncoding("Shift_JIS").GetBytes(str);
			int	i;
			for	(i=0;i<cb.Length;i++)
			{
				cb[i]	=	Jis8ToEbicdic[cb[i]];
			}
			return	cb;
		}
        /// <summary>
        /// string→EBCDIC変換
        /// </summary>
        /// <param name="str">変換元文字列</param>
        /// <param name="dest">変換結果バイト配列</param>
        /// <param name="offset">変換結果格納位置</param>
        /// <param name="leng">変換結果格納サイズ</param>
        /// <param name="pad">変換結果が格納サイズに満たない場合のパディング</param>
        public static void ToEbcdic(string str, ref byte[] dest, int offset, int leng, byte pad)
        {
            byte[] cb = System.Text.Encoding.GetEncoding("Shift_JIS").GetBytes(str);
            int i;
            for (i = 0; (i < cb.Length)&&(i<leng); i++)
            {
                if ((offset + i) < dest.Length)
                    dest[offset+i] = Jis8ToEbicdic[cb[i]];
            }
            for (; i < leng; i++)
            {
                if ((offset + i) < dest.Length)
                    dest[offset + i] = pad;
            }
        }
        /// <summary>
        /// string→EBCDIC変換
        /// </summary>
        /// <param name="str">変換元文字列</param>
        /// <param name="dest">変換結果バイト配列</param>
        /// <param name="offset">変換結果格納位置</param>
        /// <param name="leng">変換結果格納サイズ</param>
        /// <param name="pad">変換結果が格納サイズに満たない場合のパディング</param>
        public static void ToEbcdic(string str, ref byte[] dest, int offset, int leng, string pad)
        {
            byte[] bpad = ToEbcdic(pad);
            ToEbcdic(str, ref dest, offset, leng, bpad[0]);
        }
        /// <summary>
        /// string→EBCDIC変換
        /// </summary>
        /// <param name="str">変換元文字列</param>
        /// <param name="dest">変換結果バイト配列</param>
        /// <param name="offset">変換結果格納位置</param>
        public static void ToEbcdic(string str, ref byte[] dest, int offset)
        {
            byte[] cb = System.Text.Encoding.GetEncoding("Shift_JIS").GetBytes(str);
            int i;
            for (i = 0; i < cb.Length; i++)
            {
                if ((offset + i) < dest.Length)
                    dest[offset + i] = Jis8ToEbicdic[cb[i]];
            }
        }
        /// <summary>
        /// string→EBCDIC変換
        /// </summary>
        /// <param name="str">変換元文字列</param>
        /// <param name="dest">変換結果バイト配列</param>
        public static void ToEbcdic(string str, ref byte[] dest)
        {
            ToEbcdic(str, ref dest, 0);
        }

